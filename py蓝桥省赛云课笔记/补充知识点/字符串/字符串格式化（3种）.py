#format(用{}占位)
a=1
b=2
c=3
print("你{}我{}他{}".format(a,b,c))#注意是  ‘’.‘’

#f
a=1
b=2
c=3
print(f'你{a}我{b}他{c}')

#保留小数的操作(格式：{变量：保留小数点（.xf）})  （用 f）
a=1.35
b=2.65
print(f'a={a:.1f}   b={b:.1f}')

#保留   有效数字( 用 g )
a1=1.236
print(f'{a1:.2g}')

#百分比格式化（{变量：.x%}）
a1=1.236
print(f'{a1:.2%}')  #编程百分比的形式，保留两位小数

#科学计数法（以10为底）
n=122500000
print(f'{n:e}')
print(f'{n:.2e}') #科学计数法表示保留两位小数

